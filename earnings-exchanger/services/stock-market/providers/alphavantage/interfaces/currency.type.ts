/**
 * Represents a currency type.
 *
 * Note: This type definition allows any string to be a valid currency,
 * which means it doesn't provide strict type-checking against a
 * predefined list of currency codes.
 *
 * @typedef {string} Currency
 * @example
 * "USD"
 * "EUR"
 * "JPY"
 */
export type Currency = string;
